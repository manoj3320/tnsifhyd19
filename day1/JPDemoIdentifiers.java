package com.tnssi.day1;

public class JPDemoIdentifiers {

	public static void main(String[] args) {
		
		int $number = 3320; //use start with $
		System.out.println("Roll Number of  the Student : "+ $number);
	
		String studentName = "MANOJKUMAR" ; //use camelcase
		System.out.println("Name of the Student : "+ studentName);

		String STUDENTBranch = "CSIT" ; //use Mixedcase
		System.out.println("Branch of the Student : "+ STUDENTBranch);
			
	}

}
